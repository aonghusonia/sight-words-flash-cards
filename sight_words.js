const getNewWord = word => {
  const newWord = getRandomWord();
  return word === newWord ? getNewWord(word) : newWord;
};

const sw = document.getElementById('sight-word');
const showNewWord = () => {
  sw.textContent = getNewWord(sw.textContent);
};

document.addEventListener('click', showNewWord);

document.addEventListener('keypress', function (e) {
  var key = e.which || e.keyCode;
  if (key === 32) {
    // 13 is enter 32 is space
    showNewWord();
  }
});

showNewWord();
